public class Arrays {
    public static void main(String args[])
    {
        String[] nameArray;
        nameArray = new String[]{"Poovendhan","Priya","Ashwin"};//String Array;
        int[] ageArray={23,19,21};//Integer Array;
        double gradeArray[]=new double[]{9.99,9.89,9.79};//Double Array;
        System.out.println(nameArray[0]);
        System.out.println(nameArray[1]);
        System.out.println(nameArray[2]);
        System.out.println(ageArray[0]);
        System.out.println(ageArray[1]);
        System.out.println(ageArray[2]);
        System.out.println(gradeArray[0]);
        System.out.println(gradeArray[1]);
        System.out.println(gradeArray[2]);
    }
}
