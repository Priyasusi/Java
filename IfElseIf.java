public class IfElseIf {
    public static void main(String args[])
    {
        int num=10;
        if(num>0) {
            System.out.println("Number is Postive");
        }else if(num<0) {
            System.out.println("Number is Negative");
        }else {
            System.out.println("Number is Zero");
        }
    }
}
